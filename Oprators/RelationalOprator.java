package Oprators;

public class RelationalOprator {
	
	public static void main(String[] args) {
		
		int a=10;
		int b=6;
		
		System.out.println(a==b);//10==6 false
		System.out.println(a!=b);//10!=6  true
		System.out.println(a<b);//10<6 false
		System.out.println(a>b);// 10>6 true
		System.out.println(a<=b);//10<=6 false
		System.out.println(a>=b);// 10>=6 true
		
	}
	

}
